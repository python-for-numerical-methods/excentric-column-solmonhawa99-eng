import numpy as np
from scipy.optimize import bisect

def find_critical_load(L, E, A, r, c, e, sigma_allow):
    """
    מחשב את העומס המקסימלי המותר (P) על עמוד אקסצנטרי לפי נוסחת הסקנט.
    
    פרמטרים:
    L (float): אורך העמוד במ"מ
    E (float): מודול האלסטיות ב-MPa
    A (float): שטח חתך בממ"ר
    r (float): רדיוס אינרציה במ"מ
    c (float): מרחק לציר הנייטרלי לסיב הקיצוני במ"מ
    e (float): אקסצנטריות במ"מ
    sigma_allow (float): מאמץ מותר ב-MPa
    
    החזרה:
    float: העומס הקריטי P בניוטון
    """
    
    # 1. הגדרת פונקציית העזר שהשורש שלה הוא הפתרון המבוקש
    def f(P):
        # חישוב הארגומנט שבתוך ה-cos (ברדיאנים)
        # שימו לב: sec(x) = 1 / cos(x)
        term_inside_cos = (L / (2 * r)) * np.sqrt(P / (E * A))
        
        # נוסחת הסקנט לחישוב המאמץ המקסימלי
        sigma_max = (P / A) * (1 + (e * c / r**2) * (1 / np.cos(term_inside_cos)))
        
        # המטרה היא להביא את הביטוי הזה ל-0
        return sigma_max - sigma_allow

    # 2. הגדרת טווח החיפוש (ברקציה) עבור שיטת החצייה
    # גבול תחתון: עומס קרוב ל-0 (לא בדיוק 0 כדי למנוע חלוקה באפס)
    p_min = 1e-5 
    
    # גבול עליון: חסם עליון תיאורטי (עומס הקריסה של אוילר לעמוד אידיאלי)
    # P_euler = (pi^2 * E * I) / L^2, כאשר I = A * r^2
    p_max = (np.pi**2 * E * A * r**2) / L**2
    
    # בדיקת בטיחות קלה כדי לוודא שטווח החיפוש מתאים לשיטת החצייה
    # (הערכים בקצוות חייבים להיות בסימנים מנוגדים)
    if f(p_min) * f(p_max - 1e-3) > 0:
        # אם יש בעיית סימנים, נצמצם מעט את הגבול העליון
        p_max = p_max * 0.9

    # 3. הרצת שיטת החצייה למציאת השורש בדיוק הנדרש
    critical_load = bisect(f, p_min, p_max - 1e-3, xtol=1e-5)
    
    return float(critical_load)
